/**
  * @brief  Function implementing the MemorySizeTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartMemorySizeTask */
void StartMemorySizeTask(void *argument)
{
  /* USER CODE BEGIN 5 */
    uint8_t uart_buffer[100] = {0};
    uint16_t buffer_size = 0;
    size_t free_heap_size;
    uint32_t free_stack_size = 0;
    unsigned short numb_task;
    uint8_t extra_stack_use[NUMB_OF_EXTRA_STACK_BYTES+4];
    UNUSED(extra_stack_use);
  /* Infinite loop */
  for(;;)
  {

	free_stack_size 	= osThreadGetStackSpace(MemorySizeTaskHandle);
	free_heap_size 		= xPortGetFreeHeapSize();
	numb_task 			= uxTaskGetNumberOfTasks();
	buffer_size 		= sprintf((char*)uart_buffer, "Total free, maybe fragmented, Heap = %u bytes, Free stack = %lu bytes, Number of Tasks = %u\n",
	    		  	  	  	  	  	  free_heap_size, free_stack_size, numb_task);

	HAL_UART_Transmit(&huart2, uart_buffer, buffer_size, 100);

	HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_3);

	osDelay(500);
  }

  osThreadTerminate(NULL);
  /* USER CODE END 5 */
}

