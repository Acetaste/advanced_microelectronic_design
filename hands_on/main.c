/**
* @brief Function implementing the readTask thread.
* @param argument: Not used
* @retval Nones
*/
/* USER CODE END Header_StartreadTask */
void StartreadTask(void *argument)
{
  /* USER CODE BEGIN StartreadTask */
	char uart_buffer[20] ={0};
	uint8_t transmit_buffer[60] = {0};
	int buffer_len =0;
	uint16_t blink_cnt = 80, blink_time = 0;
  /* Infinite loop */
  for(;;)
  {
	for(int i = 0;i<20;i++)
	{
		HAL_UART_Receive(&huart2, &uart_buffer[i], 1, HAL_MAX_DELAY);
		if(uart_buffer[i]== '\0')
		{
			break;
		}
	}


	if(sscanf(uart_buffer, "%hu %hu", &blink_cnt, &blink_time)==2)
	{

		if(xQueueSend(UartQueueHandle,&blink_cnt,100)==errQUEUE_FULL)
		{
			buffer_len = sprintf((char*)transmit_buffer,"buffer is full!\n");
			HAL_UART_Transmit(&huart2, transmit_buffer, buffer_len, 100);
		}
		else
		{
			if(xQueueSend(UartQueueHandle,&blink_time,100)==errQUEUE_FULL)
			{
				buffer_len = sprintf((char*)transmit_buffer,"buffer is full!\n");
				HAL_UART_Transmit(&huart2, transmit_buffer, buffer_len, 100);
			}
		}

	}
	else
	{
		buffer_len = sprintf((char*)transmit_buffer,"wrong format, expected space separated unsigned short\n");
		HAL_UART_Transmit(&huart2, transmit_buffer, buffer_len, 100);
	}
  }
  osThreadTerminate(NULL);
  /* USER CODE END StartreadTask */
}

