/**
  * @brief  Function implementing the writeTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartwriteTask */
void StartwriteTask(void *argument)
{
  /* USER CODE BEGIN 5 */
	uint16_t blink_cnt = 0, blink_time = 0;
  /* Infinite loop */
  for(;;)
  {
	  osDelay(500);
	  xQueueReceive(UartQueueHandle, &blink_cnt, HAL_MAX_DELAY);
	  xQueueReceive(UartQueueHandle, &blink_time, HAL_MAX_DELAY);
	  for(int i = 0;i<blink_cnt;i++)
	  {
		  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, GPIO_PIN_SET);
		  osDelay(blink_time/2);
		  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, GPIO_PIN_RESET);
		  osDelay(blink_time/2);
	  }


  }
  osThreadTerminate(NULL);
  /* USER CODE END 5 */
}

